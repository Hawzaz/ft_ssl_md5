#include "libft.h"

void	handle_free(char *s1, char *s2, int nb)
{
	if (nb == 1)
		free(s1);
	else if (nb == 2)
		free(s2);
	else if (nb == 3)
	{
		free(s1);
		free(s2);
	}
}
