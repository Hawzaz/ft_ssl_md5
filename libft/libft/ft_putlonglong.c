#include "libft.h"

void	ft_putlonglong(long long nb)
{
	if (nb < 0)
	{
		nb = -nb;
		ft_putchar('-');
	}
	if (nb >= 10)
		ft_putlonglong(nb / 10);
	ft_putchar(nb % 10 + 48);
}
